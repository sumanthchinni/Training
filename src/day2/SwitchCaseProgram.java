package day2;

import java.util.*;

public class SwitchCaseProgram {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int a=sc.nextInt();
		switch(a) {
		case 1: System.out.println("Quarter 1");
		break;
		case 2: System.out.println("Quarter 1");
		break;
		case 3: System.out.println("Quarter 1");
		break;
		case 4: System.out.println("Quarter 2");
		break;
		case 5: System.out.println("Quarter 2");
		break;
		case 6: System.out.println("Quarter 2");
		break;
		case 7: System.out.println("Quarter 3");
		break;
		case 8: System.out.println("Quarter 3");
		break;
		case 9: System.out.println("Quarter 3");
		break;
		case 10: System.out.println("Quarter 4");
		break;
		case 11: System.out.println("Quarter 4");
		break;
		case 12: System.out.println("Quarter 4");
		break;
		default:System.out.println("Number out of range");
		break;
		}
	}

}
